  // Check if element is inside shadow DOM
  function isInShadowDOM(element) {
    let node = element;
    while (node && node !== document) {
      if (node.toString() === '[object ShadowRoot]') {
        return true;
      }
      node = node.parentNode || node.host;
    }
    return false;
  }

