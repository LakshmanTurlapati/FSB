  /**
   * Calculate relevance score for an element
   */
  function calculateElementScore(element, taskType, prioritizeViewport) {
    let score = 0;

    // Viewport bonus
    if (prioritizeViewport) {
      const rect = element.getBoundingClientRect();
      if (rect.top >= 0 && rect.bottom <= window.innerHeight &&
          rect.left >= 0 && rect.right <= window.innerWidth) {
        score += 10;
      } else if (rect.bottom > 0 && rect.top < window.innerHeight) {
        score += 5;
      }
    }

    // Purpose-based scoring
    const purpose = inferElementPurpose(element);
    if (purpose.priority === 'high') score += 8;
    else if (purpose.priority === 'medium') score += 4;
    else if (purpose.priority === 'low') score += 1;

    // Task-type alignment bonus
    if (taskType === 'search' && purpose.role === 'search-input') score += 6;
    if (taskType === 'search' && purpose.role === 'search-action') score += 6;
    if (taskType === 'form' && purpose.role.includes('input')) score += 5;
    if (taskType === 'login' && (purpose.intent === 'login' || purpose.intent === 'password' || purpose.intent === 'username')) score += 6;
    if (taskType === 'navigation' && purpose.role.includes('navigation')) score += 5;

    // Interactive element type bonus
    const tag = element.tagName;
    if (tag === 'BUTTON' || tag === 'INPUT' || tag === 'SELECT') score += 3;
    if (tag === 'A') score += 2;
    if (tag === 'TEXTAREA') score += 3;

    // Contenteditable editor bonus
    if (element.getAttribute('contenteditable') === 'true' || element.isContentEditable) score += 4;

    // Has accessible name bonus
    if (element.getAttribute('aria-label') || element.textContent?.trim()) score += 2;

    // Form association bonus
    if (element.form) score += 2;

    // Has data-testid
    if (element.hasAttribute('data-testid')) score += 3;

    // FSB-injected canvas editor elements
    if (element.dataset.fsbRole) {
      score += 15;
      if (element.dataset.fsbRole === 'document-body') score += 5;
    }

    return score;
  }

  /**
   * Get filtered elements using a 3-stage pipeline: collection -> visibility -> scoring
   */
  function getFilteredElements(options = {}) {
    const {
      maxElements = 50,
      prioritizeViewport = true,
      taskType = 'general'
    } = options;

    // Stage 1: Get potentially relevant elements
    const candidates = document.querySelectorAll(
      'button, a, input, select, textarea, ' +
      '[role="button"], [role="link"], [role="textbox"], [role="checkbox"], [role="radio"], ' +
      '[role="combobox"], [role="listbox"], [role="menuitem"], [role="tab"], [role="switch"], ' +
      '[onclick], [tabindex]:not([tabindex="-1"]), ' +
      'label, [contenteditable="true"], summary, details'
    );

    // Stage 1b: Inject canvas-based editor elements
    const candidateArray = Array.from(candidates);
    // Access isCanvasBasedEditor via FSB namespace (defined in messaging.js)
    if (FSB.isCanvasBasedEditor && FSB.isCanvasBasedEditor()) {
      const canvasEditorSelectors = [
        { selector: '.kix-page-column', role: 'document-body', label: 'Document body (canvas editor - click to place cursor, then use type tool)' },
        { selector: '.kix-appview-editor', role: 'editor-container', label: 'Editor container' },
        { selector: '.docs-title-input', role: 'document-title', label: 'Document title field' }
      ];
      for (const { selector, role, label } of canvasEditorSelectors) {
        const el = document.querySelector(selector);
        if (el && !candidateArray.includes(el)) {
          el.dataset.fsbRole = role;
          el.dataset.fsbLabel = label;
          candidateArray.push(el);
        }
      }
    }

    // Stage 2: Filter by visibility
    const visible = candidateArray.filter(el => {
      const rect = el.getBoundingClientRect();
      if (rect.width === 0 || rect.height === 0) {
        // Bypass for contenteditable
        if (el.getAttribute('contenteditable') === 'true' || el.isContentEditable) {
          const firstChild = el.firstElementChild;
          if (firstChild) {
            const childRect = firstChild.getBoundingClientRect();
            if (childRect.width > 0 && childRect.height > 0) return true;
          }
          return true;
        }
        // Bypass for FSB-injected canvas editor elements
        if (el.dataset.fsbRole) return true;
        return false;
      }

      const styles = getComputedStyle(el);
      if (styles.display === 'none') return false;
      if (styles.visibility === 'hidden') return false;
      if (parseFloat(styles.opacity) === 0) return false;
      if (el.getAttribute('aria-hidden') === 'true') return false;

      return true;
    });

    // SPA fallback: after SPA navigation, elements may exist in DOM but have
    // zero-size rects (layout not yet complete) or transitional hidden styles.
    // If visibility filtering dropped ALL candidates, relax the filter to include
    // elements that are at least in the DOM and not display:none.
    if (visible.length === 0 && candidateArray.length > 0) {
      const relaxed = candidateArray.filter(el => {
        try {
          const styles = getComputedStyle(el);
          return styles.display !== 'none';
        } catch {
          return false;
        }
      });
      if (relaxed.length > 0) {
        return relaxed.slice(0, maxElements).map(el => el);
      }
    }

    // Stage 3: Score by relevance and limit
    const scored = visible.map(el => ({
      element: el,
      score: calculateElementScore(el, taskType, prioritizeViewport)
    }));

    // Sort by score descending, take top maxElements
    scored.sort((a, b) => b.score - a.score);
    const topElements = scored.slice(0, maxElements);

    // Ensure contenteditable editors aren't crowded out
    const hasContentEditable = topElements.some(
      item => item.element.getAttribute('contenteditable') === 'true' || item.element.isContentEditable
    );
    if (!hasContentEditable) {
      const bestEditable = scored.find(
        item => item.element.getAttribute('contenteditable') === 'true' || item.element.isContentEditable
      );
      if (bestEditable) {
        topElements[topElements.length - 1] = bestEditable;
      }
    }

    return topElements.map(item => item.element);
  }

